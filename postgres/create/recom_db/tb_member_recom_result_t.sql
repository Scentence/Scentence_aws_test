CREATE TABLE TB_MEMBER_RECOM_RESULT_T (
    MEMBER_ID     BIGINT        NOT NULL,
    PERFUME_ID    BIGINT        NOT NULL,
    PERFUME_NAME  VARCHAR(200)  NOT NULL,
    RECOM_TYPE    VARCHAR(20)   NOT NULL,
    RECOM_REASON  TEXT,
    INTEREST_YN   CHAR(1),
    RECOM_DT      TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT PK_TB_MEMBER_RECOM_RESULT_T
        PRIMARY KEY (MEMBER_ID, PERFUME_ID, RECOM_DT),

    CONSTRAINT CK_TB_MEMBER_RECOM_RESULT_T_TYPE
        CHECK (RECOM_TYPE IN ('GENERAL', 'LAYERING')),

    CONSTRAINT CK_TB_MEMBER_RECOM_RESULT_T_INTEREST
        CHECK (INTEREST_YN IN ('Y', 'N'))
);

-- FOREIGN KEY (MEMBER_ID) REFERENCES TB_MEMBER_BASIC_M (MEMBER_ID)
-- FOREIGN KEY (PERFUME_ID) REFERENCES TB_PERFUME_BASIC_M (PERFUME_ID)